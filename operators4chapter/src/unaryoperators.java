public class unaryoperators {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(++a);
        int b = 10;
        System.out.println(--b);
        int c = 15;
        System.out.println(c++);
        System.out.println(c);
        int d = 20;
        System.out.println(d--);
        System.out.println(d);

        int y = 100;
        int z = -y;
        System.out.println(z);

    }
}
